def _build_resource(
    *,
    service_name: str,
    service_version: str | None,
    environment: str | None,
    extra: Mapping[str, str],
) -> Resource:
    """Build the OTel ``Resource`` shared across signal providers.

    Centralised so traces, metrics, and logs all carry identical
    resource attributes -- otherwise correlation across signals on
    a single service breaks silently.
    """
    attrs: dict[str, str] = {"service.name": service_name}
    if service_version is not None:
        attrs["service.version"] = service_version
    if environment is not None:
        attrs["deployment.environment.name"] = environment
    attrs.update(extra)
    return Resource.create(attrs)


def _build_sampler(*, name: str, ratio: float | None) -> Any:
    """Translate the schema's sampler name to an SDK sampler.

    Validation already constrained ``name`` to a known string and
    ratio to ``[0.0, 1.0]`` when applicable; this function trusts
    that and is meant to be a thin lookup.
    """
    if name == "always_on":
        return ALWAYS_ON
    if name == "always_off":
        return ALWAYS_OFF
    if name == "parentbased_always_on":
        return ParentBased(ALWAYS_ON)
    if name == "parentbased_always_off":
        return ParentBased(ALWAYS_OFF)
    if name == "traceidratio":
        assert ratio is not None  # noqa: S101 -- guaranteed by config validator
        return TraceIdRatioBased(ratio)
    if name == "parentbased_traceidratio":
        assert ratio is not None  # noqa: S101 -- guaranteed by config validator
        return ParentBased(TraceIdRatioBased(ratio))
    msg = f"Unknown sampler {name!r}"
    raise ValueError(msg)


def _build_span_exporter(*, exporter: str | None) -> Any | None:
    """Build a span exporter from the schema's selection.

    Returns ``None`` when ``exporter`` is ``"none"`` (caller skips
    the BatchSpanProcessor).  Otherwise the exporter is constructed
    with no arguments -- the OTel SDK reads ``OTEL_EXPORTER_OTLP_*``
    natively, so duplicating that here would just be churn.

    The gRPC exporter is imported lazily because its protobuf /
    grpc-io stack is roughly an order of magnitude heavier than the
    HTTP transport.  HTTP-only consumers don't pay that import
    cost; gRPC consumers install the
    ``kiln-generator[opentelemetry-grpc]`` extra to make the import
    succeed.
    """
    if exporter == "none":
        return None
    if exporter == "console":
        return ConsoleSpanExporter()
    if exporter == "otlp_grpc":
        from opentelemetry.exporter.otlp.proto.grpc.trace_exporter import (  # type: ignore[import-not-found]  # noqa: PLC0415
            OTLPSpanExporter as GrpcSpanExporter,
        )

        return GrpcSpanExporter()
    # Default and ``otlp_http``: HTTP/protobuf transport.
    return HttpSpanExporter()


def build_tracer_provider(
    *,
    service_name: str,
    service_version: str | None = None,
    environment_env: str | None = None,
    resource_attributes: Mapping[str, str] | None = None,
    sampler: str = "parentbased_always_on",
    sampler_ratio: float | None = None,
    exporter: str | None = None,
    exporter_endpoint_env: str = "OTEL_EXPORTER_OTLP_ENDPOINT",
    exporter_headers_env: str = "OTEL_EXPORTER_OTLP_HEADERS",
) -> TracerProvider:
    """Build and return a configured ``TracerProvider``.

    The caller is responsible for installing it via
    :func:`opentelemetry.trace.set_tracer_provider`.  Splitting
    construction from installation keeps the helper testable
    without touching the global tracer provider.

    ``environment_env`` is the *name* of the environment variable
    holding the deployment-environment value (e.g. ``"ENVIRONMENT"``),
    not the value itself -- the same generated artifact deploys
    across dev / staging / prod, so the value is resolved at startup.
    ``None`` skips the lookup; an unset / empty variable does the same.
    """
    resource = _build_resource(
        service_name=service_name,
        service_version=service_version,
        environment=_resolve_env(environment_env),
        extra=resource_attributes or {},
    )
    provider = TracerProvider(
        resource=resource,
        sampler=_build_sampler(sampler, sampler_ratio),
    )
    span_exporter = _build_span_exporter(
        exporter, exporter_endpoint_env, exporter_headers_env
    )
    if span_exporter is not None:
        provider.add_span_processor(BatchSpanProcessor(span_exporter))
    return provider


def build_meter_provider(
    *,
    service_name: str,
    service_version: str | None = None,
    environment_env: str | None = None,
    resource_attributes: Mapping[str, str] | None = None,
) -> MeterProvider:
    """Build a ``MeterProvider`` reading exporter config from env.

    The metrics exporter is always selected via the standard
    ``OTEL_METRICS_EXPORTER`` env var (default ``otlp``); we don't
    expose a per-config override because metrics deployments
    almost universally pair with the same OTLP endpoint as traces.
    """
    resource = _build_resource(
        service_name=service_name,
        service_version=service_version,
        environment=_resolve_env(environment_env),
        extra=resource_attributes or {},
    )
    reader = PeriodicExportingMetricReader(OTLPMetricExporter())
    return MeterProvider(resource=resource, metric_readers=[reader])


def build_logger_provider(
    *,
    service_name: str,
    service_version: str | None = None,
    environment_env: str | None = None,
    resource_attributes: Mapping[str, str] | None = None,
) -> LoggerProvider:
    """Build a ``LoggerProvider`` configured via OTLP env vars.

    Off by default in :class:`~kiln.config.schema.TelemetryConfig`;
    only used when the consumer opts in to log export.  The provider
    on its own is not enough -- the generated ``init_telemetry`` also
    attaches a :class:`opentelemetry.sdk._logs.LoggingHandler` to the
    stdlib root logger so ``logging.getLogger().info(...)`` calls
    flow through OTLP.
    """
    resource = _build_resource(
        service_name=service_name,
        service_version=service_version,
        environment=_resolve_env(environment_env),
        extra=resource_attributes or {},
    )
    provider = LoggerProvider(resource=resource)
    provider.add_log_record_processor(
        BatchLogRecordProcessor(OTLPLogExporter())
    )
    return provider



"""Generator for pgcraft view/function stubs and FastAPI view routes.

For each :class:`~kiln.config.schema.ViewModel` in the config two
files are produced:

1. **pgcraft stub** (``db/views/<name>.py``, ``overwrite=False``) —
   a scaffold the developer fills in with the actual SQL query or
   function body.  Non-parameterised views get a
   ``PGCraftViewMixin`` stub; parameterised ones get a
   ``PGCraftFunctionMixin`` stub.

2. **FastAPI route** (``api/views/<name>.py``, ``overwrite=True``) —
   queries the named database object without any inline SQL:

   - Views: ``select(view_table)``
   - Functions: ``func.<schema>.<name>(params).table_valued(cols)``
"""

from __future__ import annotations

from kiln.config.schema import KilnConfig, ViewModel
from kiln.generators._helpers import (
    PG_SQL_TYPES,
    PYTHON_TYPES,
    SA_INSTANCE_TYPES,
)
from kiln.generators.base import GeneratedFile


class ViewGenerator:
    """Generates pgcraft stubs and FastAPI routes for views/functions."""

    @property
    def name(self) -> str:
        """Unique generator identifier."""
        return "views"

    def can_generate(self, config: KilnConfig) -> bool:
        """Return True when the config has at least one view.

        Args:
            config: The validated kiln configuration.
        """
        return bool(config.views)

    def generate(self, config: KilnConfig) -> list[GeneratedFile]:
        """Produce stub and route files for each view in *config*.

        Args:
            config: The validated kiln configuration.

        Returns:
            Two :class:`~kiln.generators.base.GeneratedFile` objects
            per view: a pgcraft stub and a FastAPI route file.
        """
        files: list[GeneratedFile] = []
        for view in config.views:
            files.append(
                GeneratedFile(
                    path=f"db/views/{view.name}.py",
                    content=_render_stub(view, config.module),
                    overwrite=False,
                )
            )
            files.append(
                GeneratedFile(
                    path=f"api/views/{view.name}.py",
                    content=_render_route(view, config.module),
                    overwrite=True,
                )
            )
        return files


# ---------------------------------------------------------------------------
# Stub rendering
# ---------------------------------------------------------------------------


def _render_stub(view: ViewModel, module: str) -> str:
    """Render the pgcraft stub file for *view*.

    Args:
        view: The view configuration.
        module: Root module name for generated import paths.

    Returns:
        Python source string (scaffold, not a complete definition).
    """
    if view.parameters:
        return _render_function_stub(view, module)
    return _render_view_stub(view, module)


def _render_view_stub(view: ViewModel, module: str) -> str:
    """Stub for a non-parameterised pgcraft view."""
    col_lines = "\n".join(
        f"    Column({SA_INSTANCE_TYPES[c.type]}),"
        f"  # {c.name}"
        for c in view.returns
    )
    col_defs = "\n".join(
        f"    Column(\"{c.name}\", {SA_INSTANCE_TYPES[c.type]}),"
        for c in view.returns
    )
    return f'''\
"""pgcraft view stub for {view.name}.

Generated by kiln.  This file will NOT be overwritten on re-generation.

Fill in ``__query__`` with a SQLAlchemy ``select()`` expression that
produces the columns declared below, then run pgcraft migrations.

The ``{view.name}_table`` object at the bottom is a lightweight
SQLAlchemy ``Table`` used by the generated FastAPI route to query
the view — it does not need to change.
"""

from __future__ import annotations

from sqlalchemy import Column, MetaData, Table, func, select  # noqa: F401
import sqlalchemy.dialects.postgresql as pg  # noqa: F401
from pgcraft import PGCraftViewMixin

from {module}.db.base import Base
# from {module}.db.models.<model> import <Model>  # uncomment as needed


# ---------------------------------------------------------------------------
# pgcraft view definition — fill in __query__ before running migrations
# ---------------------------------------------------------------------------

# class {_pascal(view.name)}(PGCraftViewMixin, Base):
#     __tablename__ = "{view.name}"
#     __table_args__ = {{"schema": "{view.schema}"}}
#
#     # Output columns (declare explicitly for accurate ORM mapping)
{_comment_block(col_lines)}
#
#     # TODO: replace with the actual query
#     __query__ = select(
#         ...
#     )


# ---------------------------------------------------------------------------
# SQLAlchemy Table used by the FastAPI route to query this view
# ---------------------------------------------------------------------------

{view.name}_table = Table(
    "{view.name}",
    MetaData(),
{col_defs}
    schema="{view.schema}",
)
'''


def _render_function_stub(view: ViewModel, module: str) -> str:
    """Stub for a parameterised set-returning function."""
    returns_sql = ", ".join(
        f"{c.name} {PG_SQL_TYPES[c.type]}" for c in view.returns
    )
    return f'''\
"""pgcraft function stub for {view.name}.

Generated by kiln.  This file will NOT be overwritten on re-generation.

Fill in the SQL ``definition`` below, then run pgcraft migrations.

The FastAPI route calls this function via SQLAlchemy's
``func.{view.schema}.{view.name}(...).table_valued(...)`` — no SQL
appears in the route file.
"""

from __future__ import annotations

from sqlalchemy_declarative_extensions import register_function
from sqlalchemy_declarative_extensions.dialects.postgresql import Function

from {module}.db.base import Base


# ---------------------------------------------------------------------------
# pgcraft function definition — fill in definition before running migrations
# ---------------------------------------------------------------------------

register_function(
    Base.metadata,
    Function(
        "{view.name}",
        # TODO: replace with the actual SQL body.
        # Parameters are bound positionally: $1, $2, ... in SQL language,
        # or by name in PL/pgSQL.
        """
        -- SELECT ...
        """,
        returns="TABLE({returns_sql})",
        schema="{view.schema}",
        language="sql",
    ),
)
'''



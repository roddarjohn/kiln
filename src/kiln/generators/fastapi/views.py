def _render_route(view: ViewModel, module: str) -> str:
    """Render the FastAPI route file for *view*.

    Args:
        view: The view configuration.
        module: Root module name for generated import paths.

    Returns:
        Python source string.
    """
    if view.parameters:
        return _render_function_route(view, module)
    return _render_view_route(view, module)


def _render_view_route(view: ViewModel, module: str) -> str:
    """Route that queries a non-parameterised view via its SA Table."""
    result_class = _pascal(view.name) + "Result"
    has_auth = view.require_auth
    method = view.http_method.lower()
    slug = view.name.replace("_", "-")

    needs_uuid = any(c.type == "uuid" for c in view.returns)
    needs_dt = any(c.type == "datetime" for c in view.returns)
    needs_date = any(c.type == "date" for c in view.returns)
    needs_any = any(c.type == "json" for c in view.returns)

    lines: list[str] = [
        f'"""FastAPI route for the {view.name} view.',
        "",
        "Generated by kiln — re-run ``kiln generate`` to update.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
    ]
    if needs_uuid:
        lines.append("import uuid")
    dt_parts = []
    if needs_dt:
        dt_parts.append("datetime")
    if needs_date:
        dt_parts.append("date")
    if dt_parts:
        lines.append(f"from datetime import {', '.join(dt_parts)}")
    if needs_any:
        lines.append("from typing import Any")
    lines.append("from typing import Annotated")
    lines.append("")
    lines.append("from fastapi import APIRouter, Depends")
    lines.append("from pydantic import BaseModel")
    lines.append("from sqlalchemy import select")
    lines.append("from sqlalchemy.ext.asyncio import AsyncSession")
    lines.append("")
    if has_auth:
        lines.append(
            f"from {module}.auth.dependencies import get_current_user"
        )
    lines.append(f"from {module}.db.session import get_db")
    lines.append(
        f"from {module}.db.views.{view.name} import {view.name}_table"
    )
    lines.append("")
    lines.append(
        f'router = APIRouter(prefix="/{view.name.replace("_", "-")}", '
        f'tags=["{view.name.replace("_", "-")}"])'
    )
    lines.append(
        "DB = Annotated[AsyncSession, Depends(get_db)]"
    )
    if has_auth:
        lines.append(
            "CurrentUser = Annotated[dict, Depends(get_current_user)]"
        )
    lines.append("")
    lines.append("")

    # Response model
    lines.append(f"class {result_class}(BaseModel):")
    lines.append(f'    """Response row from the {view.name} view."""')
    lines.append("")
    for col in view.returns:
        lines.append(f"    {col.name}: {PYTHON_TYPES[col.type]}")
    lines.append("")
    lines.append('    model_config = {"from_attributes": True}')
    lines.append("")
    lines.append("")

    # Route handler
    auth_param = "\n    _: CurrentUser," if has_auth else ""
    desc = view.description or f"Query the {view.name} view."
    lines.append(
        f'@router.{method}("/", response_model=list[{result_class}])'
    )
    lines.append(f"async def get_{view.name}(")
    lines.append(f"    db: DB,{auth_param}")
    lines.append(f") -> list[{result_class}]:")
    lines.append(f'    """{desc}"""')
    lines.append(
        f"    result = await db.execute(select({view.name}_table))"
    )
    lines.append(
        f"    return [{result_class}.model_validate(dict(r._mapping))"
        " for r in result.all()]"
    )
    lines.append("")
    return "\n".join(lines)


def _render_function_route(view: ViewModel, module: str) -> str:
    """Route that calls a set-returning function via func.table_valued."""
    result_class = _pascal(view.name) + "Result"
    has_auth = view.require_auth
    method = view.http_method.lower()

    needs_uuid = any(
        c.type == "uuid" for c in list(view.parameters) + list(view.returns)
    )
    needs_dt = any(
        c.type == "datetime"
        for c in list(view.parameters) + list(view.returns)
    )
    needs_date = any(
        c.type == "date"
        for c in list(view.parameters) + list(view.returns)
    )
    needs_any = any(c.type == "json" for c in view.returns)

    lines: list[str] = [
        f'"""FastAPI route for the {view.name} function.',
        "",
        "Generated by kiln — re-run ``kiln generate`` to update.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
    ]
    if needs_uuid:
        lines.append("import uuid")
    dt_parts = []
    if needs_dt:
        dt_parts.append("datetime")
    if needs_date:
        dt_parts.append("date")
    if dt_parts:
        lines.append(f"from datetime import {', '.join(dt_parts)}")
    if needs_any:
        lines.append("from typing import Any")
    lines.append("from typing import Annotated")
    lines.append("")
    lines.append("from fastapi import APIRouter, Depends")
    lines.append("from pydantic import BaseModel")
    lines.append("from sqlalchemy import column, func, select")
    lines.append("import sqlalchemy.dialects.postgresql as pg")
    lines.append("from sqlalchemy.ext.asyncio import AsyncSession")
    lines.append("")
    if has_auth:
        lines.append(
            f"from {module}.auth.dependencies import get_current_user"
        )
    lines.append(f"from {module}.db.session import get_db")
    lines.append("")
    slug = view.name.replace("_", "-")
    lines.append(
        f'router = APIRouter(prefix="/{slug}", tags=["{slug}"])'
    )
    lines.append("DB = Annotated[AsyncSession, Depends(get_db)]")
    if has_auth:
        lines.append(
            "CurrentUser = Annotated[dict, Depends(get_current_user)]"
        )
    lines.append("")
    lines.append("")

    # Response model
    lines.append(f"class {result_class}(BaseModel):")
    lines.append(
        f'    """Response row from the {view.name} function."""'
    )
    lines.append("")
    for col in view.returns:
        lines.append(f"    {col.name}: {PYTHON_TYPES[col.type]}")
    lines.append("")
    lines.append("")

    # table_valued column specs
    tv_cols = ", ".join(
        f'column("{c.name}", {SA_INSTANCE_TYPES[c.type]})'
        for c in view.returns
    )
    # Function call args (positional, matching parameter order)
    fn_args = ", ".join(p.name for p in view.parameters)

    # Route signature params
    sig_params = [
        f"    {p.name}: {PYTHON_TYPES[p.type]}" for p in view.parameters
    ]
    sig_params.append("    db: DB")
    if has_auth:
        sig_params.append("    _: CurrentUser")

    desc = view.description or f"Call the {view.name} function."
    col_names = ", ".join(f'"{c.name}"' for c in view.returns)

    lines.append(
        f'@router.{method}("/", response_model=list[{result_class}])'
    )
    lines.append(f"async def {view.name}(")
    for p in sig_params:
        lines.append(f"{p},")
    lines.append(f") -> list[{result_class}]:")
    lines.append(f'    """{desc}"""')
    lines.append(f"    fn = func.{view.schema}.{view.name}(")
    lines.append(f"        {fn_args}")
    lines.append(f"    ).table_valued({tv_cols})")
    lines.append("    result = await db.execute(select(fn))")
    lines.append(
        f"    return [{result_class}(**{{k: getattr(row, k)"
        f" for k in [{col_names}]}})"
    )
    lines.append("            for row in result.all()]")
    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _pascal(name: str) -> str:
    """Convert snake_case *name* to PascalCase."""
    return "".join(part.capitalize() for part in name.split("_"))


def _comment_block(text: str) -> str:
    """Prefix each line of *text* with ``# ``."""
    return "\n".join(f"# {line}" if line.strip() else "#" for line in text.splitlines())
